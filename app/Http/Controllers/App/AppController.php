    {
        $books = \App\Book::orderBy('book_name', 'ASC')->paginate(12);

        foreach ($books as $book) {
            if($book->book_image == 'noimage.png'){
                $book->book_image = 'defaultimage.jpg';
            }
        }

        return view('app.book.index', compact('books'));
    }

